-- ─── Poll Accesses table ─────────────────────────────────────
-- Tracks QR code scans with IP-based geolocation (privacy-safe)
create table if not exists public.poll_accesses (
  id          uuid        primary key default gen_random_uuid(),
  poll_id     text        not null references public.polls(id) on delete cascade,
  lat         numeric(9,6),
  lng         numeric(9,6),
  province    text,
