-- ─── Extensions ────────────────────────────────────────────
create extension if not exists "uuid-ossp";

-- ─── News table ─────────────────────────────────────────────
-- Stores all news items fetched by the Node API (notimarket-api-node)
create table if not exists public.news (
  id           text        primary key,
  title        text        not null,
  summary      text,
  source       text,
  published_at timestamptz,
  url          text        not null unique,
  sentiment    text        not null default 'neutral',
  topic        text        not null default 'politica',
  fetched_at   timestamptz not null default now()
);

comment on table public.news is 'News items fetched from the upstream news-intelligence-api';

-- ─── Polls table ─────────────────────────────────────────────
-- Stores polls generated by the bot from news items
create table if not exists public.polls (
  id           text        primary key,
  news_id      text        references public.news(id) on delete set null,
  news_title   text        not null,
  news_url     text        not null,
  news_source  text,
  question     text        not null,
  options      jsonb       not null,   -- Array of { id, text, votes }
  topic        text        not null,
  sentiment    text        not null,
  province     text,                   -- DR province code (e.g. 'SDQ', 'STI')
  total_votes  integer     not null default 0,
  created_at   timestamptz not null default now(),
  created_by   uuid        references auth.users(id) on delete set null
);

comment on table public.polls is 'Polls generated from news items by the NotiMarket bot';

-- ─── Votes table ─────────────────────────────────────────────
-- One vote per user per poll
create table if not exists public.votes (
  id           uuid        primary key default gen_random_uuid(),
  poll_id      text        not null references public.polls(id) on delete cascade,
  user_id      uuid        not null references auth.users(id) on delete cascade,
  option_id    text        not null,
  option_index integer     not null,
  created_at   timestamptz not null default now(),
  unique (poll_id, user_id)
);

comment on table public.votes is 'User votes – one per poll per authenticated user';

-- ─── Row Level Security ──────────────────────────────────────
alter table public.news   enable row level security;
alter table public.polls  enable row level security;
alter table public.votes  enable row level security;

-- News: public read, service role write (backend upserts via service key)
create policy "News: public read"
  on public.news for select using (true);

-- Polls: public read, authenticated users can insert
create policy "Polls: public read"
  on public.polls for select using (true);

create policy "Polls: authenticated insert"
  on public.polls for insert
  with check (auth.uid() is not null);

-- Votes: users see own votes, can insert own votes
create policy "Votes: user reads own"
  on public.votes for select
  using (auth.uid() = user_id);

create policy "Votes: user inserts own"
  on public.votes for insert
  with check (auth.uid() = user_id);

-- ─── Indexes ─────────────────────────────────────────────────
create index if not exists news_topic_idx       on public.news  (topic);
create index if not exists news_fetched_at_idx  on public.news  (fetched_at desc);
create index if not exists polls_topic_idx      on public.polls (topic);
create index if not exists polls_province_idx   on public.polls (province);
create index if not exists polls_created_at_idx on public.polls (created_at desc);
create index if not exists votes_poll_id_idx    on public.votes (poll_id);
create index if not exists votes_user_id_idx    on public.votes (user_id);

-- ─── Profiles table ──────────────────────────────────────────
-- One row per user; created automatically via trigger on signup
create table if not exists public.profiles (
  id          uuid        primary key references auth.users(id) on delete cascade,
  full_name   text,
  avatar_url  text,
  role        text        not null default 'user' check (role in ('user', 'admin')),
  created_at  timestamptz not null default now()
);

comment on table public.profiles is 'User profiles with roles (user | admin)';

-- ─── Helper function (avoids RLS recursion) ──────────────────
create or replace function public.is_admin()
returns boolean as $$
  select exists (
    select 1 from public.profiles
    where id = auth.uid() and role = 'admin'
  );
$$ language sql security definer stable;

-- ─── Trigger: auto-create profile on signup ──────────────────
create or replace function public.handle_new_user()
returns trigger as $$
begin
  insert into public.profiles (id, full_name, avatar_url)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'full_name', new.raw_user_meta_data->>'name'),
    new.raw_user_meta_data->>'avatar_url'
  )
  on conflict (id) do nothing;
  return new;
end;
$$ language plpgsql security definer;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

-- ─── Profiles RLS ────────────────────────────────────────────
alter table public.profiles enable row level security;

create policy "Profiles: authenticated read"
  on public.profiles for select
  using (auth.uid() is not null);

create policy "Profiles: insert own"
  on public.profiles for insert
  with check (auth.uid() = id);

create policy "Profiles: user updates own"
  on public.profiles for update
  using (auth.uid() = id)
  with check (role = (select role from public.profiles where id = auth.uid()));

create policy "Profiles: admin updates any"
  on public.profiles for update
  using (public.is_admin());

create index if not exists profiles_role_idx on public.profiles (role);

