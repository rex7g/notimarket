  ip_hash     text,                   -- First 3 octets of IP (e.g. "190.22.14")
  ref         text        not null default 'direct'
                          check (ref in ('qr', 'direct')),
  created_at  timestamptz not null default now()
);

comment on table public.poll_accesses is 'QR scan events with geolocation for heatmap analytics';

alter table public.poll_accesses enable row level security;

-- Anyone (including anonymous) can insert when scanning a QR
create policy "PollAccesses: anon insert"
  on public.poll_accesses for insert
  with check (true);

-- Only admins can read scan analytics
create policy "PollAccesses: admin select"
  on public.poll_accesses for select
  using (public.is_admin());

create index if not exists poll_accesses_poll_id_idx    on public.poll_accesses (poll_id);
create index if not exists poll_accesses_province_idx   on public.poll_accesses (province);
create index if not exists poll_accesses_created_at_idx on public.poll_accesses (created_at desc);

